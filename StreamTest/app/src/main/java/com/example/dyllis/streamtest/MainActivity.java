    public String getExternalPath() {
        String sdPath ;
        String ext = Environment.getExternalStorageState();
        if (ext.equals(Environment.MEDIA_MOUNTED)) {
            sdPath = Environment.getExternalStorageDirectory().getAbsolutePath();
        } else
            sdPath = ""+getFilesDir();
        return sdPath;
    }
